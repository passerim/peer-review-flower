class PrConfig:
    """This class holds some configuration values and constants used by other
    classes in the library."""

    # Default FitIns dictionary flag to enable review.
    REVIEW_FLAG: str = "review"
    # Default FitIns dictionary entry for the review score.
    REVIEW_SCORE: str = "review_score"
    # Default maximum number of review rounds.
    MAX_REVIEW_ROUNDS: int = 5
